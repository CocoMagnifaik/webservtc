package service;

/**
 *
 * @author Coco
 */
public class Categorie {
    String id;
    String categorie;

    public Categorie(String id, String categorie) {
        this.id = id;
        this.categorie = categorie;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCategorie() {
        return categorie;
    }

    public void setCategorie(String categorie) {
        this.categorie = categorie;
    }
}
